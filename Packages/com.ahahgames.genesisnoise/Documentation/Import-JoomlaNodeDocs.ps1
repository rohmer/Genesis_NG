            -PayloadOverrides $categoryPayloadOverrides

        $categoryIdByRelativePath[$category.RelativePath] = $ensuredCategory.Id
    }

    Write-Step ("Resolved {0} Joomla categories." -f ($categoryIdByRelativePath.Keys.Count - 1))

    for ($index = 0; $index -lt $articleDefinitions.Count; $index++) {
        $article = $articleDefinitions[$index]
        $categoryId = $categoryIdByRelativePath[$article.CategoryRelativePath]
        $html = Convert-NodeMarkdownToHtml -Article $article -ArticleUrlMap @{}

        Write-Progress -Activity "Importing Joomla articles" -Status $article.Title -PercentComplete ((($index + 1) / [math]::Max(1, $articleDefinitions.Count)) * 100)

        $articleInfo = Ensure-Article `
            -Article $article `
            -CategoryId $categoryId `
            -Html $html `
            -PayloadOverrides $articlePayloadOverrides

        $articleInfoByRelativePath[$article.RelativePath.ToLowerInvariant()] = [pscustomobject]@{
            Id = $articleInfo.Id
            Alias = $article.Alias
            Title = $article.Title
            CategoryId = $categoryId
        }
    }

    Write-Progress -Activity "Importing Joomla articles" -Completed

    foreach ($relativePath in $articleInfoByRelativePath.Keys) {
        $info = $articleInfoByRelativePath[$relativePath]
        $articleUrlMap[$relativePath] = Build-ArticleUrl -ArticleId $info.Id -CategoryId $info.CategoryId
    }

    for ($index = 0; $index -lt $articleDefinitions.Count; $index++) {
        $article = $articleDefinitions[$index]
        $categoryId = $categoryIdByRelativePath[$article.CategoryRelativePath]
        $html = Convert-NodeMarkdownToHtml -Article $article -ArticleUrlMap $articleUrlMap

        Write-Progress -Activity "Patching Joomla article links" -Status $article.Title -PercentComplete ((($index + 1) / [math]::Max(1, $articleDefinitions.Count)) * 100)

        Ensure-Article `
            -Article $article `
            -CategoryId $categoryId `
            -Html $html `
            -PayloadOverrides $articlePayloadOverrides `
            -LinkPatch | Out-Null
    }

    Write-Progress -Activity "Patching Joomla article links" -Completed
}
else {
    Write-Step "Article import skipped by request."
}

