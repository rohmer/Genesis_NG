function Restart-InPwshIfNeeded {
    if ($PSVersionTable.PSVersion.Major -ge 7 -and (Get-Command ConvertFrom-Markdown -ErrorAction SilentlyContinue)) {
        return
    }

    if ($RelaunchedInPwsh) {
        throw "This script requires PowerShell 7+ with the Microsoft.PowerShell.Utility module available. The current shell still does not provide ConvertFrom-Markdown."
    }

    $pwsh = Get-Command pwsh -ErrorAction SilentlyContinue
    if ($null -eq $pwsh) {
        throw "This script requires PowerShell 7+. 'pwsh' was not found on PATH, and the current shell does not provide ConvertFrom-Markdown."
    }

    $forwardedArgs = New-Object System.Collections.Generic.List[string]
    $forwardedArgs.Add("-NoProfile")
    $forwardedArgs.Add("-ExecutionPolicy")
    $forwardedArgs.Add("Bypass")
    $forwardedArgs.Add("-File")
    $forwardedArgs.Add($PSCommandPath)

    foreach ($entry in $PSBoundParameters.GetEnumerator() | Sort-Object Key) {
        if ($entry.Key -eq "RelaunchedInPwsh") {
            continue
        }

        if ($entry.Value -is [switch]) {
            if ($entry.Value.IsPresent) {
                $forwardedArgs.Add("-$($entry.Key)")
            }
            continue
        }

        if ($null -ne $entry.Value) {
            $forwardedArgs.Add("-$($entry.Key)")
            $forwardedArgs.Add([string]$entry.Value)
        }
    }

    $forwardedArgs.Add("-RelaunchedInPwsh")
    Write-Host "Current shell does not support this importer. Restarting it with PowerShell 7 (`pwsh`)."
    & $pwsh.Source @forwardedArgs
    exit $LASTEXITCODE
}

