function Normalize-SiteBaseUrl {
    param([string]$Url)

    if ([string]::IsNullOrWhiteSpace($Url)) {
        return ""
    }

    $normalized = $Url.Trim()
    $normalized = $normalized.TrimEnd("/")
    $normalized = [regex]::Replace($normalized, "/api/index\.php/v1/?$", "", [System.Text.RegularExpressions.RegexOptions]::IgnoreCase)
    return $normalized.TrimEnd("/")
}

function Join-UrlPath {
    param(
        [string]$Base,
        [string]$Relative
    )

    $cleanRelative = ($Relative -replace "\\", "/").TrimStart("/")

    if ([string]::IsNullOrWhiteSpace($Base)) {
        return "/" + $cleanRelative
    }

    return $Base.TrimEnd("/") + "/" + $cleanRelative
}

function Get-RelativePath {
    param(
        [string]$BasePath,
        [string]$TargetPath
    )

    $baseFullPath = [IO.Path]::GetFullPath($BasePath)
    if (-not $baseFullPath.EndsWith([IO.Path]::DirectorySeparatorChar)) {
        $baseFullPath += [IO.Path]::DirectorySeparatorChar
    }

    $targetFullPath = [IO.Path]::GetFullPath($TargetPath)
    $baseUri = [Uri]$baseFullPath
    $targetUri = [Uri]$targetFullPath
    return [Uri]::UnescapeDataString($baseUri.MakeRelativeUri($targetUri).ToString()).Replace("\", "/")
}

function Get-ObjectPropertyValue {
    param(
        [object]$Object,
        [string]$Name,
        $Default = $null
    )

    if ($null -eq $Object) {
        return $Default
    }

    $property = $Object.PSObject.Properties[$Name]
    if ($null -eq $property) {
        return $Default
    }

    return $property.Value
}

function Get-JoomlaItems {
    param([object]$Response)

    if ($null -eq $Response) {
        return @()
    }

    $data = Get-ObjectPropertyValue -Object $Response -Name "data"
    if ($null -eq $data) {
        return @($Response)
    }

    if ($data -is [System.Array]) {
        return @($data)
    }

    return @($data)
}

function New-CategoryLookupKey {
    param(
        [string]$ParentId,
        [string]$Alias
    )

    return ("{0}|{1}" -f $ParentId, $Alias).ToLowerInvariant()
}

function Read-OptionalJsonHashtable {
    param([string]$Path)

    if ([string]::IsNullOrWhiteSpace($Path)) {
        return @{}
    }

    if (-not (Test-Path $Path)) {
        throw "JSON payload override file not found: $Path"
    }

    return (Get-Content -Raw -Path $Path | ConvertFrom-Json -AsHashtable)
}

function Merge-Hashtables {
    param(
        [hashtable]$Base,
        [hashtable]$Override
    )

    $result = @{}

    foreach ($entry in $Base.GetEnumerator()) {
        $result[$entry.Key] = $entry.Value
    }

    foreach ($entry in $Override.GetEnumerator()) {
        if ($result.ContainsKey($entry.Key) -and
            $result[$entry.Key] -is [hashtable] -and
            $entry.Value -is [hashtable]) {
            $result[$entry.Key] = Merge-Hashtables -Base $result[$entry.Key] -Override $entry.Value
        }
        else {
            $result[$entry.Key] = $entry.Value
        }
    }

    return $result
}

function Invoke-JoomlaApi {
    param(
        [ValidateSet("GET", "POST", "PATCH")]
        [string]$Method,
        [string]$Path,
        [object]$Body
    )

    if ($DryRun) {
        throw "Invoke-JoomlaApi should not be called during dry runs."
    }

    $uri = Join-UrlPath -Base $script:apiRoot -Relative $Path
    $invokeArgs = @{
        Uri = $uri
        Method = $Method
        Headers = $script:headers
        TimeoutSec = 120
    }

    if ($PSBoundParameters.ContainsKey("Body") -and $null -ne $Body) {
        $invokeArgs.Body = ($Body | ConvertTo-Json -Depth 25 -Compress)
    }

    try {
        return Invoke-RestMethod @invokeArgs
    }
    catch {
        $details = if (-not [string]::IsNullOrWhiteSpace($_.ErrorDetails.Message)) {
            $_.ErrorDetails.Message
        }
        else {
            $_.Exception.Message
        }

        throw "Joomla API $Method $uri failed: $details"
    }
}

function Refresh-CategoryCache {
    if ($DryRun) {
        return
    }

    $script:categoryCache = @{}
    $response = Invoke-JoomlaApi -Method GET -Path "content/categories?page%5Blimit%5D=1000"

    foreach ($item in (Get-JoomlaItems $response)) {
        $attributes = Get-ObjectPropertyValue -Object $item -Name "attributes" -Default $item
        $alias = [string](Get-ObjectPropertyValue -Object $attributes -Name "alias" -Default "")
        $parentId = [string](Get-ObjectPropertyValue -Object $attributes -Name "parent_id" -Default (Get-ObjectPropertyValue -Object $attributes -Name "parentId" -Default ""))

        if ([string]::IsNullOrWhiteSpace($alias) -or [string]::IsNullOrWhiteSpace($parentId)) {
            continue
        }

        $key = New-CategoryLookupKey -ParentId $parentId -Alias $alias
        $script:categoryCache[$key] = [pscustomobject]@{
            Id = [string](Get-ObjectPropertyValue -Object $item -Name "id" -Default "")
            Alias = $alias
            Title = [string](Get-ObjectPropertyValue -Object $attributes -Name "title" -Default "")
            ParentId = $parentId
        }
    }
}

function Get-ArticlesForCategory {
    param([string]$CategoryId)

    if ($script:articleCacheByCategoryId.ContainsKey($CategoryId)) {
        return $script:articleCacheByCategoryId[$CategoryId]
    }

    if ($DryRun) {
        $script:articleCacheByCategoryId[$CategoryId] = @{}
        return $script:articleCacheByCategoryId[$CategoryId]
    }

    $path = "content/articles?filter%5Bcategory%5D=$CategoryId&page%5Blimit%5D=1000"
    $response = Invoke-JoomlaApi -Method GET -Path $path
    $lookup = @{}

    foreach ($item in (Get-JoomlaItems $response)) {
        $attributes = Get-ObjectPropertyValue -Object $item -Name "attributes" -Default $item
        $alias = [string](Get-ObjectPropertyValue -Object $attributes -Name "alias" -Default "")

        if ([string]::IsNullOrWhiteSpace($alias)) {
            continue
        }

        $lookup[$alias.ToLowerInvariant()] = [pscustomobject]@{
            Id = [string](Get-ObjectPropertyValue -Object $item -Name "id" -Default "")
            Alias = $alias
            Title = [string](Get-ObjectPropertyValue -Object $attributes -Name "title" -Default "")
            CategoryId = $CategoryId
        }
    }

    $script:articleCacheByCategoryId[$CategoryId] = $lookup
    return $lookup
}

function Get-TopLevelCategoryTitleMap {
    $map = @{}
    $categoryPages = Get-ChildItem -Path $DocsRoot -File -Filter *.md |
        Where-Object { $_.BaseName -ne "README" -and -not $_.BaseName.StartsWith("_") }

    foreach ($page in $categoryPages) {
        $firstLine = Get-Content -Path $page.FullName -TotalCount 1
        if ($firstLine -match '^#\s+Genesis Nodes:\s+(.+)$') {
            $map[$page.BaseName.ToLowerInvariant()] = $Matches[1].Trim()
        }
    }

    return $map
}

function Format-TitleFromSlug {
    param([string]$Slug)

    $textInfo = [System.Globalization.CultureInfo]::InvariantCulture.TextInfo
    $parts = $Slug -split "-"
    $words = foreach ($part in $parts) {
        if ($part -match '^\d') {
            $part.ToUpperInvariant()
        }
        else {
            $textInfo.ToTitleCase($part)
        }
    }

    return ($words -join " ").Trim()
}

function Get-CategoryDefinitions {
    $topLevelTitleMap = Get-TopLevelCategoryTitleMap
    $definitions = New-Object System.Collections.Generic.List[object]

    foreach ($directory in (Get-ChildItem -Path $nodesRoot -Directory -Recurse | Sort-Object FullName)) {
        $relativePath = Get-RelativePath -BasePath $nodesRoot -TargetPath $directory.FullName
        $segments = $relativePath -split "/"
        $alias = $segments[$segments.Count - 1]
        $parentRelativePath = if ($segments.Count -gt 1) { (($segments | Select-Object -SkipLast 1) -join "/") } else { "" }
        $title = if ($segments.Count -eq 1 -and $topLevelTitleMap.ContainsKey($alias.ToLowerInvariant())) {
            $topLevelTitleMap[$alias.ToLowerInvariant()]
        }
        else {
            Format-TitleFromSlug -Slug $alias
        }

        $definitions.Add([pscustomobject]@{
            RelativePath = $relativePath
            ParentRelativePath = $parentRelativePath
            Alias = $alias
            Title = $title
            Depth = $segments.Count
        })
    }

    return [object[]]($definitions | Sort-Object Depth, RelativePath)
}

function Get-FirstHeading {
    param([string]$Markdown)

    foreach ($line in ($Markdown -split "`r?`n")) {
        if ($line -match '^#\s+(.+)$') {
            return $Matches[1].Trim()
        }
    }

    return $null
}

function Get-ArticleDefinitions {
    $definitions = New-Object System.Collections.Generic.List[object]
    $files = Get-ChildItem -Path $nodesRoot -Recurse -File -Filter *.md | Sort-Object FullName

    foreach ($file in $files) {
        $relativePath = Get-RelativePath -BasePath $nodesRoot -TargetPath $file.FullName
        $categoryRelativePath = Split-Path -Path $relativePath -Parent
        $markdown = Get-Content -Raw -Path $file.FullName
        $title = Get-FirstHeading -Markdown $markdown

        if ([string]::IsNullOrWhiteSpace($title)) {
            $title = Format-TitleFromSlug -Slug $file.BaseName
        }

        $definitions.Add([pscustomobject]@{
            FilePath = $file.FullName
            RelativePath = $relativePath
            CategoryRelativePath = ($categoryRelativePath -replace "\\", "/")
            Alias = $file.BaseName
            Title = $title
            Markdown = $markdown
        })
    }

    return [object[]]$definitions
}

function Remove-GeneratedPreamble {
    param([string]$Markdown)

    $lines = $Markdown -split "`r?`n"
    $filtered = New-Object System.Collections.Generic.List[string]
    $removedTitle = $false

    foreach ($line in $lines) {
        if (-not $removedTitle -and $line -match '^#\s+') {
            $removedTitle = $true
            continue
        }

        if ($line -match '^>\s+This file is auto-generated') {
            continue
        }

        if ($line -match '^\[Back to index\]\(.+\)\s*\|\s*\[Back to .+\]\(.+\)\s*$') {
            continue
        }

        $filtered.Add($line)
    }

    return (($filtered -join "`n").Trim())
}

function Convert-NodeMarkdownToHtml {
    param(
        [psobject]$Article,
        [hashtable]$ArticleUrlMap
    )

    $markdown = Remove-GeneratedPreamble -Markdown $Article.Markdown

    $markdown = [regex]::Replace($markdown, '!\[(?<alt>[^\]]*)\]\((?<target>(?:\.\./)+_images/(?<path>[^)]+))\)', {
        param($match)

        $alt = $match.Groups["alt"].Value
        $relativeImagePath = $match.Groups["path"].Value.Replace("\", "/")
        $remoteUrl = Join-UrlPath -Base $RemoteImageUrlBase -Relative $relativeImagePath
        return "![{0}]({1})" -f $alt, $remoteUrl
    })

    $markdown = [regex]::Replace($markdown, '\[(?<label>[^\]]+)\]\((?<target>(?:\.\./)+Runtime/[^)]+)\)', {
        param($match)

        $label = $match.Groups["label"].Value
        $relativeRuntimePath = ($match.Groups["target"].Value -replace '^(?:\.\./)+', '').Replace("\", "/")

        if (-not [string]::IsNullOrWhiteSpace($SourceUrlBase)) {
            $sourceUrl = Join-UrlPath -Base $SourceUrlBase -Relative $relativeRuntimePath
            return "[{0}]({1})" -f $label, $sourceUrl
        }

        return "``{0}``" -f $label
    })

    $markdown = [regex]::Replace($markdown, '\[(?<label>[^\]]+)\]\((?<target>(?:\.\./)+_nodes/(?<path>[^)]+\.md))\)', {
        param($match)

        $label = $match.Groups["label"].Value
        $relativeArticlePath = $match.Groups["path"].Value.Replace("\", "/").ToLowerInvariant()

        if ($null -ne $ArticleUrlMap -and $ArticleUrlMap.ContainsKey($relativeArticlePath)) {
            return "[{0}]({1})" -f $label, $ArticleUrlMap[$relativeArticlePath]
        }

        return $label
    })

    $converted = ConvertFrom-Markdown -InputObject $markdown
    return $converted.Html.Trim()
}

