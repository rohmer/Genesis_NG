<#
.SYNOPSIS
Bulk-imports generated Genesis node documentation into Joomla.

.DESCRIPTION
This script imports the generated node pages under Documentation/Nodes/_nodes into
Joomla articles via Joomla's content API. It also supports mirroring node screenshot
files into a Joomla site's /images tree when that path is mounted locally.

The importer expects the documentation generated by:
    Documentation/Generate-GenesisNodeDocs.ps1

It performs the import in two passes:
1. Create or update categories and articles.
2. Patch article bodies again so any node-to-node markdown links can be rewritten
   using the real Joomla article IDs.

.EXAMPLE
pwsh ./Documentation/Import-JoomlaNodeDocs.ps1 `
  -JoomlaBaseUrl "https://example.com" `
  -ApiToken "YOUR_TOKEN" `
  -JoomlaImageRootPath "\\server\joomla\images\genesis-nodes"

.EXAMPLE
pwsh ./Documentation/Import-JoomlaNodeDocs.ps1 -DryRun -Verbose

.NOTES
- Joomla article import uses the content API under /api/index.php/v1/content/articles
- Category import uses the content API under /api/index.php/v1/content/categories
- If your Joomla site needs extra payload fields such as workflow configuration,
  pass them in via -CategoryPayloadJsonPath and/or -ArticlePayloadJsonPath
#>

