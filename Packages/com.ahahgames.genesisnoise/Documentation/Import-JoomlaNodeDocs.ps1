function Ensure-Category {
    param(
        [string]$Title,
        [string]$Alias,
        [string]$ParentId,
        [hashtable]$PayloadOverrides
    )

    $key = New-CategoryLookupKey -ParentId $ParentId -Alias $Alias

    if ($script:categoryCache.ContainsKey($key)) {
        $script:categoryStats.Reused++
        return $script:categoryCache[$key]
    }

    if ($DryRun) {
        $script:nextDryCategoryId++
        $category = [pscustomobject]@{
            Id = [string]$script:nextDryCategoryId
            Alias = $Alias
            Title = $Title
            ParentId = $ParentId
        }
        $script:categoryCache[$key] = $category
        $script:categoryStats.Created++
        return $category
    }

    $payload = @{
        title = $Title
        alias = $Alias
        extension = "com_content"
        parent_id = [int]$ParentId
        language = $Language
        published = 1
    }

    $payload = Merge-Hashtables -Base $payload -Override $PayloadOverrides
    $response = Invoke-JoomlaApi -Method POST -Path "content/categories" -Body $payload
    $createdItem = (Get-JoomlaItems $response | Select-Object -First 1)
    $createdAttributes = Get-ObjectPropertyValue -Object $createdItem -Name "attributes" -Default $createdItem

    $category = [pscustomobject]@{
        Id = [string](Get-ObjectPropertyValue -Object $createdItem -Name "id" -Default "")
        Alias = [string](Get-ObjectPropertyValue -Object $createdAttributes -Name "alias" -Default $Alias)
        Title = [string](Get-ObjectPropertyValue -Object $createdAttributes -Name "title" -Default $Title)
        ParentId = $ParentId
    }

    if ([string]::IsNullOrWhiteSpace($category.Id)) {
        throw "Category creation succeeded but Joomla did not return a category id for '$Title'."
    }

    $script:categoryCache[$key] = $category
    $script:categoryStats.Created++
    return $category
}

function Ensure-Article {
    param(
        [psobject]$Article,
        [string]$CategoryId,
        [string]$Html,
        [hashtable]$PayloadOverrides,
        [switch]$LinkPatch
    )

    $articleLookup = Get-ArticlesForCategory -CategoryId $CategoryId
    $lookupKey = $Article.Alias.ToLowerInvariant()

    if ($articleLookup.ContainsKey($lookupKey)) {
        $existing = $articleLookup[$lookupKey]

        if ($DryRun) {
            if ($LinkPatch) {
                $script:articleStats.LinkPatched++
            }
            else {
                $script:articleStats.UpdatedExisting++
            }
            return $existing
        }

        $payload = @{
            id = [int]$existing.Id
            title = $Article.Title
            alias = $Article.Alias
            catid = [int]$CategoryId
            introtext = $Html
            fulltext = ""
            language = $Language
            metadesc = ""
            metakey = ""
        }

        $payload = Merge-Hashtables -Base $payload -Override $PayloadOverrides
        Invoke-JoomlaApi -Method PATCH -Path ("content/articles/{0}" -f $existing.Id) -Body $payload | Out-Null
        if ($LinkPatch) {
            $script:articleStats.LinkPatched++
        }
        else {
            $script:articleStats.UpdatedExisting++
        }
        return $existing
    }

    if ($DryRun) {
        $script:nextDryArticleId++
        $created = [pscustomobject]@{
            Id = [string]$script:nextDryArticleId
            Alias = $Article.Alias
            Title = $Article.Title
            CategoryId = $CategoryId
        }
        $articleLookup[$lookupKey] = $created
        $script:articleStats.Created++
        return $created
    }

    $payload = @{
        title = $Article.Title
        alias = $Article.Alias
        articletext = $Html
        catid = [int]$CategoryId
        language = $Language
        metadesc = ""
        metakey = ""
    }

    $payload = Merge-Hashtables -Base $payload -Override $PayloadOverrides
    $response = Invoke-JoomlaApi -Method POST -Path "content/articles" -Body $payload
    $createdItem = (Get-JoomlaItems $response | Select-Object -First 1)
    $createdAttributes = Get-ObjectPropertyValue -Object $createdItem -Name "attributes" -Default $createdItem

    $created = [pscustomobject]@{
        Id = [string](Get-ObjectPropertyValue -Object $createdItem -Name "id" -Default "")
        Alias = [string](Get-ObjectPropertyValue -Object $createdAttributes -Name "alias" -Default $Article.Alias)
        Title = [string](Get-ObjectPropertyValue -Object $createdAttributes -Name "title" -Default $Article.Title)
        CategoryId = $CategoryId
    }

    if ([string]::IsNullOrWhiteSpace($created.Id)) {
        throw "Article creation succeeded but Joomla did not return an article id for '$($Article.Title)'."
    }

    $articleLookup[$lookupKey] = $created
    $script:articleStats.Created++
    return $created
}

function Copy-ImagesToJoomlaRoot {
    param([string]$DestinationRoot)

    $actualImages = @()
    if (Test-Path $imagesRoot) {
        $actualImages = Get-ChildItem -Path $imagesRoot -Recurse -File |
            Where-Object { $_.Extension -ne ".meta" }
    }

    if ($actualImages.Count -eq 0) {
        Write-Step "No screenshot files were found under $imagesRoot. Skipping image copy."
        return
    }

    if ([string]::IsNullOrWhiteSpace($DestinationRoot)) {
        Write-Step "Image copy skipped because -JoomlaImageRootPath was not provided."
        $script:imageStats.Skipped += $actualImages.Count
        return
    }

    foreach ($image in $actualImages) {
        $relativePath = Get-RelativePath -BasePath $imagesRoot -TargetPath $image.FullName
        $targetPath = Join-Path $DestinationRoot ($relativePath -replace "/", "\")
        $targetDirectory = Split-Path -Parent $targetPath

        if ($DryRun) {
            $script:imageStats.Copied++
            continue
        }

        if (-not (Test-Path $targetDirectory)) {
            New-Item -ItemType Directory -Path $targetDirectory -Force | Out-Null
        }

        Copy-Item -Path $image.FullName -Destination $targetPath -Force
        $script:imageStats.Copied++
    }
}

